package com.explane2;

public class Variables {

	public static void main(String[] args) {
		String message = "Hello world!";

		byte a = -123;
		short b = 32_765;
		int c = 345_134_213;
		long d = -14_341_234_123L;

		float e = -3.343434F;
		double f = -12_323.434;

		char g = 'F';

		boolean h = true;

		System.out.println(message + " " + a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + g + " " + h);
	}
}
